package Clases;

public class Comandas {

    private int id_comanda;
    private int NumeroComanda;
    private String Nombre;
    private String Direccion;
    private String NumeroEntradas;
    private String Entrada;
    private String NumeroPlatos;
    private String Platos;
    private String NumeroBebidas;
    private String Bebidas;
    private String Descripcion;

    public int getId_comanda() {
        return id_comanda;
    }

    public void setId_comanda(int id_comanda) {
        this.id_comanda = id_comanda;
    }

    public int getNumeroComanda() {
        return NumeroComanda;
    }

    public void setNumeroComanda(int NumeroComanda) {
        this.NumeroComanda = NumeroComanda;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getDireccion() {
        return Direccion;
    }

    public void setDireccion(String Direccion) {
        this.Direccion = Direccion;
    }

    public String getNumeroEntradas() {
        return NumeroEntradas;
    }

    public void setNumeroEntradas(String NumeroEntradas) {
        this.NumeroEntradas = NumeroEntradas;
    }

    public String getEntrada() {
        return Entrada;
    }

    public void setEntrada(String Entrada) {
        this.Entrada = Entrada;
    }

    public String getNumeroPlatos() {
        return NumeroPlatos;
    }

    public void setNumeroPlatos(String NumeroPlatos) {
        this.NumeroPlatos = NumeroPlatos;
    }

    public String getPlatos() {
        return Platos;
    }

    public void setPlatos(String Platos) {
        this.Platos = Platos;
    }

    public String getNumeroBebidas() {
        return NumeroBebidas;
    }

    public void setNumeroBebidas(String NumeroBebidas) {
        this.NumeroBebidas = NumeroBebidas;
    }

    public String getBebidas() {
        return Bebidas;
    }

    public void setBebidas(String Bebidas) {
        this.Bebidas = Bebidas;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }
    
    
    
}
